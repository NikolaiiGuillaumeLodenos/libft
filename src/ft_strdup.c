#include "libft.h"

char	*ft_strdup(char const *s)
{
	char	*tmp;

	if (!s)
		return (NULL);
	tmp = (char *)malloc(sizeof(*tmp) * ft_strlen(s) + 1);
	return (tmp = ft_strcpy(tmp, s));
}
